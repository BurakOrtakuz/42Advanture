void	*ft_memchr(const char *s, int c)
{
	char	*p;

	p = (void *)s;
	while (s[0] != '\0')
	{
		if (s[0] == c)
		{
			p = (void *)s;
			break ;
		}
		s++;
	}
	return (p);
}
