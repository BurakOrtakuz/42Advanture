#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;

	p = (char *)s;
	while (s[0] != '\0')
	{
		if (s[0] == c)
		{
			p = (char *)s;
		}
		s++;
	}
	return (p);
}
